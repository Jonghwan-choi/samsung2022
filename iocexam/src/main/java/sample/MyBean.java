package sample;

public class MyBean {
	private String name;
	private int count;
	
	MyBean(){
		
	}
	MyBean(String name){
		this.name = name;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getCount() {
		return count;
	}
	public void setCount(int count) {
		this.count = count;
	}
	
	

}
